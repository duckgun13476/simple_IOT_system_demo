    oled.fill_rect(x,y,12,12,0)
    for i in range(12):
        for j in range(12):
            byte_index = i * 2 + j // 8
            bit_index = 7 - j % 8
            k = (data_123[byte_index] >> bit_index) & 0x01
            if nu==0:
                if k==0:
                    k=1
                else:
                    k=0
            if k == 1:
                oled.pixel(j+x,i+y,1)
